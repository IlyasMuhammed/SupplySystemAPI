// ── Test builder ──────────────────────────────────────────────────────────────

file static class QuoteBuild
{
    internal static (QuotationRepository repo, DemandDbContext db) New(Action<DemandDbContext>? seed = null)
    {
        var opts = new DbContextOptionsBuilder<DemandDbContext>()
            .UseInMemoryDatabase(Guid.NewGuid().ToString())
            .Options;
        var db = new DemandDbContext(opts);
        seed?.Invoke(db);
        db.SaveChanges();
        return (new QuotationRepository(db), db);
    }

    internal static CreateQuotationRequest QuotationRequest(
        string sourceType = "STANDALONE",
        Guid? sourceId = null,
        params CreateQuotationLineRequest[] lines) => new()
    {
        Title      = "Test RFQ",
        SourceType = sourceType,
        SourceId   = sourceId,
        DueDate    = DateTime.UtcNow.AddDays(14),
        Lines      = lines.Length > 0 ? [.. lines] : [DefaultLine()]
    };

    internal static CreateQuotationLineRequest DefaultLine(
        string description = "Laptop",
        decimal qty = 2m) => new()
    {
        ItemDescription = description,
        UnitOfMeasure   = "PC",
        Quantity        = qty
    };

    internal static SendQuotationRequest SendRequest(Guid? supplierId = null) => new()
    {
        Suppliers = [new InviteSupplierRequest
        {
            SupplierId   = supplierId ?? Guid.NewGuid(),
            SupplierName = "ACME Corp"
        }]
    };

    internal static RecordVendorResponseRequest VendorResponse(
        Guid quotationLineUuid,
        decimal netUnitPrice,
        decimal qty = 1m,
        string supplierName = "Test Vendor") => new()
    {
        SupplierId   = Guid.NewGuid(),
        SupplierName = supplierName,
        Lines =
        [
            new VendorResponseLineRequest
            {
                QuotationLineUuid = quotationLineUuid,
                NetUnitPrice      = netUnitPrice,
                Quantity          = qty
            }
        ]
    };
}

